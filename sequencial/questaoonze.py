n1 = int(input('Digite um número inteiro: '))
n2 = int(input('Digite outro número inteiro: '))
n3 = float(input('Digite um número real: '))

Jujuba = (2 * n1) * (n2 / 2)
Gabs = (3 * n1) + n3
Amor = n3 ** 3

print(f'O produto do dobro do primeiro número com a metade do segundo é igual a: {Jujuba}')
print(f'A soma do triplo do primeiro com o terceiro é igual a: {Gabs}')
print(f'O terceiro número elevado ao cubo é igual a: {Amor}') 

